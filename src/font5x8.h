byte* font5x8(int index) {
  int length = FONT_5X8_HEIGHT;
  for (int i = 0; i < length; i++) {
    font5x8Buffer[i] = pgm_read_byte(&font5x8Array[index - 48][i]);
  }
  font5x8Buffer[length] = '\0';
  return font5x8Buffer;
}

